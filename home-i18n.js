function linkFor(lang, target) {
  return lang === "jp" ? target : target === "index.html" ? `index-${lang}.html` : target;
}

function cardList(items) {
  return items.map(([title, text], index) => `
    <article class="card highlight">
      <div class="icon-box">${String(index + 1).padStart(2, "0")}</div>
      <h3>${title}</h3>
      <p>${text}</p>
    </article>
  `).join("");
}

